=head1 DESCRIPTION

Sydney Rail Map: Sydney M1 Line.

    +----------------------+-----------------------------------+
    | Station Name         | Linked To                         |
    +----------------------+-----------------------------------+
    | Tallawong            | Rouse Hill                        |
    | Rouse Hill           | Tallawong, Kellyville             |
    | Kellyville           | Rouse Hill, Bella Vista           |
    | Bella Vista          | Kellyville, Norwest               |
    | Norwest              | Bella Vista, Hills Showground     |
    | Hills Showground     | Norwest, Castle Hill              |
    | Castle Hill          | Hills Showground, Cherrybrook     |
    | Cherrybrook          | Castle Hill, Epping               |
    | Epping               | Cherrybrook, Macquarie University |
    | Macquarie University | Epping, Macquarie Park            |
    | Macquarie Park       | Macquarie University, North Ryde  |
    | North Ryde           | Macquarie Park, Chatswood         |
    | Chatswood            | North Ryde, Crows Nest            |
    | Crows Nest           | Chatswood, Victoria Cross         |
    | Victoria Cross       | Crows Nest, Barangaroo            |
    | Barangaroo           | Victoria Cross, Martin Place      |
    | Martin Place         | Barangaroo, Gadigal               |
    | Gadigal              | Martin Place, Central             |
    | Central              | Gadigal, Waterloo                 |
    | Waterloo             | Central, Sydenham                 |
    | Sydenham             | Waterloo                          |
    +----------------------+-----------------------------------+

=head1 NOTE

=over 2

=item * The station "Central" is also part of
          L<T1 Line|Map::Tube::Sydney::Line::T1>
        | L<T2 Line|Map::Tube::Sydney::Line::T2>
        | L<T3 Line|Map::Tube::Sydney::Line::T3>
        | L<T4 Line|Map::Tube::Sydney::Line::T4>
        | L<T8 Line|Map::Tube::Sydney::Line::T8>
        | L<T9 Line|Map::Tube::Sydney::Line::T9>

=item * The station "Chatswood" is also part of
          L<T1 Line|Map::Tube::Sydney::Line::T1>
        | L<T9 Line|Map::Tube::Sydney::Line::T9>

=item * The station "Epping" is also part of
          L<T9 Line|Map::Tube::Sydney::Line::T9>

=item * The station "Martin Place" is also part of
          L<T4 Line|Map::Tube::Sydney::Line::T4>

=item * The station "Sydenham" is also part of
          L<T4 Line|Map::Tube::Sydney::Line::T4>
        | L<T8 Line|Map::Tube::Sydney::Line::T8>

=item * The line is currently being extended from Sydenham to Bankstown, with the planned opening due in 2026.

=back

