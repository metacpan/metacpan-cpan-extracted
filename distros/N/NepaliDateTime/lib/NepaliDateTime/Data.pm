# ---------------------------------------------------------------------------
# Build lookup tables
# ---------------------------------------------------------------------------

# %DAYS_IN_MONTH{year}[1..12]   = days in that month
# @DAYS_BEFORE_YEAR[0..N]       = ordinal of first day of each year (0-based offset from BS 1975-01-01)
# %CUMUL{year}[0..12]           = cumulative days before each month in that year
#   [0]=0, [1]=m1, [2]=m1+m2, ... [12]=total days in year

our %DAYS_IN_MONTH;   # $DAYS_IN_MONTH{year}[month]  (1-based)
our %CUMUL;           # $CUMUL{year}[month]           (0-based prefix sums)
our @DAYS_BEFORE_YEAR;# [index] = total days before year MINYEAR+index

{
    my $running = 0;
    for my $row (@RAW) {
        my ($year, @months) = @$row;
        $DAYS_IN_MONTH{$year} = [undef, @months];  # index 1..12
        my @cum = (0);
        my $total = 0;
        for my $d (@months) { $total += $d; push @cum, $total; }
        $CUMUL{$year} = \@cum;
        push @DAYS_BEFORE_YEAR, $running;
        $running += $total;
    }
    push @DAYS_BEFORE_YEAR, $running;  # sentinel: total days in range
}

our $MAXORDINAL = $DAYS_BEFORE_YEAR[-1];

