=head1 NAME

CracTools::DigitagCT::Structure - A structure to store DGE tags.

=head1 SYNOPSIS


=head1 DESCRIPTION

A structure to store DGE tags.

=cut

package CracTools::DigitagCT::Structure;

use strict;
use warnings;

use CracTools::Utils;

sub new {
  my $class = shift;
  
  my $self = bless {
    tags => {}, 
  }, $class;
  
  return $self
}

sub addTag {
  my $self = shift;
  my $SAMline = shift;
  my $tag = $SAMline->seq;

  if($SAMline->getStrand eq -1) {
    $tag = CracTools::Utils::reverseComplement($tag);
  }

  if(defined $self->{tags}{$tag}) {
    $self->{tags}{$tag}{occ}++;
  } else {
    $self->{tags}{$tag}{line} = $SAMline;
    $self->{tags}{$tag}{occ} = 1;
  }
}

sub nbOccurences {
  my $self = shift;
  my $seq = shift;
  my $format = shift;
  my $nbOcc = 0;

  if(defined $seq) {
    $nbOcc = $self->{tags}{$seq}{occ};
  } else {
    foreach my $tag (keys %{$self->{tags}}) {
      $nbOcc += $self->{$tag}{occ};
    }
  }
  return $nbOcc;
}

sub getSAMLine {
  my $self = shift;
  my $seq = shift;
  return $self->{tags}{$seq}{line};
}

=head2 iterator

  Example     : my $it = $sam_reader->iterator();
                while(my $line = $it->()) {
                  print $line->seq,"\n";
                }
  Description : Create an iterator to go throud each lines of the file
  ReturnType  : Iterator on CracTools::SAMline
  Exceptions  : none

=cut

sub iterator {
  my $self = shift;
  my @keys = keys %{$self->{tags}};

  return sub {
    if(@keys) {
      my $key = $keys[0];
      shift @keys;
      return $key;
      #return $self{tags}{$keys[0]};
    } else {
      return undef;
    }
  };
}

sub addGenericElement {
  my $self = shift;
  my ($tag,$element_name,$element) = @_;
  if(defined $self->{tags}{$tag}) {
    $self->{tags}{$tag}{$element_name} = $element;
  }
}

sub getGenericElement {
  my $self = shift;
  my ($tag,$element_name) = @_;
  if(defined $self->{tags}{$tag}) {
    return $self->{tags}{$tag}{$element_name};
  } else {
    return undef;
  }
}

1;
