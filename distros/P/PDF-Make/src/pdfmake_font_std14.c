/*============================================================================
 * API implementation
 *==========================================================================*/

const pdfmake_std14_data_t *pdfmake_std14_get(pdfmake_std14_id_t id) {
    if (id < 0 || id >= PDFMAKE_STD14_COUNT) return NULL;
    return std14_fonts[id];
}

/* Note: pdfmake_std14_lookup() is defined in pdfmake_page.c */

int pdfmake_std14_width(pdfmake_std14_id_t id, uint32_t codepoint) {
    const pdfmake_std14_data_t *data = pdfmake_std14_get(id);
    size_t lo, hi;

    if (!data) return 0;

    /* Binary search for codepoint (table is sorted) */
    lo = 0;
    hi = data->width_count;
    while (lo < hi) {
        size_t mid = lo + (hi - lo) / 2;
        if (data->widths[mid].codepoint < codepoint) {
            lo = mid + 1;
        } else if (data->widths[mid].codepoint > codepoint) {
            hi = mid;
        } else {
            return data->widths[mid].width;
        }
    }
    
    /* Not found - return default width */
    /* Most fonts use space width as default */
    if (data->width_count > 0) {
        /* First entry is usually space */
        return data->widths[0].width;
    }
    return 500;  /* Fallback */
}
