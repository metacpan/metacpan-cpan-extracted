use strict;
use vars qw($aiu @core @mbcs $dot $range $cap @found);

use FindBin;
use lib "$FindBin::Bin/../../lib";
use mb;
mb::set_script_encoding('sjis');

# Shift_JIS 铆聻聢毛聺录锚掳聙毛聜聵 3锚掳聹: a(\x82\xA0) i(\x82\xA2) u(\x82\xA4).
$aiu = "\x82\xA0\x82\xA2\x82\xA4";

# CORE "."毛聤聰 毛掳聰矛聺麓铆聤赂 铆聲聵毛聜聵 -- 6毛掳聰矛聺麓铆聤赂矛聴聬 6矛隆掳锚掳聛.
@core = ($aiu =~ /(.)/g);
print "CORE /(.)/g      : ", scalar(@core), " pieces (bytes)\n";   # 6

# mb::qr("(.)")毛聤聰 "."毛楼录 铆聲聹 毛卢赂矛聻聬毛隆聹 毛搂聦毛聯聽毛聥陇 -- 3矛隆掳锚掳聛. 铆聲聹 毛虏聢
# 矛禄麓铆聦聦矛聺录铆聲聹 毛聮陇 矛禄麓铆聦聦矛聺录毛聬聹 铆聦篓铆聞麓矛聺聞 毛搂陇矛鹿颅矛聴聬 毛聜麓矛聜陆铆聲聹毛聥陇.
$dot  = mb::qr("(.)");
@mbcs = ($aiu =~ /$dot/g);
print "mb::qr /(.)/g    : ", scalar(@mbcs), " pieces (chars)\n";   # 3

# 铆聻聢毛聺录锚掳聙毛聜聵 毛赂聰毛隆聺 a..n (\x82\xA0-\x82\xF1)矛聴聬 毛聦聙铆聲聹 毛卢赂矛聻聬-铆聛麓毛聻聵矛聤陇 毛虏聰矛聹聞.
# 毛虏聰矛聹聞毛聤聰 毛卢赂矛聻聬 矛聽聞矛虏麓毛楼录 毛鹿聞锚碌聬铆聲聵毛炉i掳毛隆聹 u毛聤聰 矛聲聢矛聴聬 矛聻聢锚鲁聽 ASCII "A"毛聤聰 矛聲聞毛聥聢毛聥陇.
$range = mb::qr("[\x82\xA0-\x82\xF1]");
print "u in [a-n]       : ", ("\x82\xA4" =~ /$range/ ? 1 : 0), "\n";   # 1
print "A in [a-n]       : ", ("A"         =~ /$range/ ? 1 : 0), "\n";   # 0

# 矛潞隆矛虏聵毛聤聰 毛漏聙铆聥掳毛掳聰矛聺麓铆聤赂 毛卢赂矛聻聬 矛聽聞矛虏麓(矛聴卢锚赂掳矛聞聹毛聤聰 2毛掳聰矛聺麓铆聤赂)毛楼录 毛掳聵铆聶聵铆聲聵毛漏掳
# 矛聽聢毛聦聙 矛聽聢毛掳聵矛聺聞 毛掳聵铆聶聵铆聲聵矛搂聙 矛聲聤毛聤聰毛聥陇.
$cap = mb::qr("([\x82\xA0-\x82\xF1])");
if ("X\x82\xA2Y" =~ /$cap/) {
    print "captured char    : ", length($1), " bytes\n";   # 2
}

# 铆聵录铆聲漏 毛卢赂矛聻聬矛聴麓矛聴聬矛聞聹 毛陋篓毛聯聽 铆聻聢毛聺录锚掳聙毛聜聵毛楼录 毛卢赂矛聻聬 毛聥篓矛聹聞毛隆聹 矛掳戮毛聤聰毛聥陇.
@found = ("a\x82\xA0b\x82\xA2c" =~ /$cap/g);
print "hiragana found   : ", scalar(@found), "\n";   # 2

exit 0;
