int aria_keysize(int *keysize)
{
   LTC_ARGCHK(keysize != NULL);
   if (*keysize >= 32) *keysize = 32;
   else if (*keysize >= 24) *keysize = 24;
   else if (*keysize >= 16) *keysize = 16;
   else return CRYPT_INVALID_KEYSIZE;
   return CRYPT_OK;
}

