use Daje::Database::Model::Login;
use Daje::Database::Model::UsersVerificationCodes;
use Daje::Database::Model::CompaniesUsers;
use POSIX;

use Data::Dumper;

use Digest::SHA qw{sha512_base64};

our $VERSION = "0.01";

has 'db';
has 'config';

async sub login_user ($self, $mail, $password) {
    my $result;

    return $result = {
        mail  => $mail,
        status  => 'no_acceptable_password'
    } unless length($password) > 7;

    $password = sha512_base64 $password;

    my $login = Daje::Database::Model::Login->new(
        db => $self->db
    )->login(
        $mail, $password
    );

    my $jwt;
    $login->{password} = $password;
    if(defined $login and exists $login->{users_users_pkey}) {
        $jwt = await Daje::Tools::JWT->new(
            secret => @{$self->config->{secrets}}[0]
        )->encode_jwt_p(
            $login
        );
        my $logged_in = strftime "%Y-%m-%d %H:%M:%S", localtime time;
        $result = {
            mail                => $mail,
            jwt                 => $jwt,
            logged_in           => $logged_in,
            status              => $login->{status},
            users_workflow_fkey => $login->{users_workflow_fkey},
            users_users_pkey    => $login->{users_users_pkey},
            avatar              => $login->{avatar},
            name                => $login->{name},
        };
    } elsif (defined $login and exists $login->{status}) {
        $result = {
            mail  => $mail,
            status  => $login->{status}
        };
    }
    return $result;
}

async sub post_login($self, $login ) {
    my $claim  = await Daje::Tools::JWT->new(
        secret => @{$self->config->{secrets}}[0]
    )->decode_jwt_p(
        $login->{data}->{jwt}
    );
    $claim->{companies_companies_pkey} = $login->{data}->{companies_companies_pkey};
    $claim->{users_users_pkey} = $login->{data}->{users_users_pkey};
    my $jwt = await Daje::Tools::JWT->new(
         secret => @{$self->config->{secrets}}[0]
     )->encode_jwt_p(
        $claim
     );

    my $logged_in = strftime "%Y-%m-%d %H:%M:%S", localtime time;
    my $result = {
        mail                     => $login->{data}->{mail},
        jwt                      => $jwt,
        logged_in                => $logged_in,
        status                   => $login->{data}->{status},
        users_workflow_fkey      => $login->{users_workflow_fkey},
        users_users_pkey         => $login->{data}->{users_users_pkey},
        companies_companies_pkey => $login->{data}->{companies_companies_pkey},
        avatar                   => $login->{data}->{avatar},
        name                     => $login->{data}->{name},
    };

    return $result;
}

async sub check_verify($self, $mail, $verification_code) {
    return 1 if Daje::Database::Model::UsersVerificationCodes->new(
        db => $self->db
    )->check_verify(
        $mail, $verification_code
    );
    return 0;
}

sub verify($self, $mail) {

    my @set = ('0' ..'9', 'A' .. 'F');
    my $verification_code = join '' => map $set[rand @set], 1 .. 4;

    try {
        Daje::Database::Model::UsersVerificationCodes->new(
            db => $self->db
        )->save_verification_code(
            $mail, $verification_code
        );
    } catch($e) {
        say $e;
        $self->app->log>error($e);
    };

    return $verification_code;
}

sub authenticate ($self, $payload) {
    my $login;
    try {
        my $claim = Daje::Tools::JWT->new(
            secret => @{$self->config->{secrets}}[0]
        )->decode_jwt(
            $payload
        );
        $login = Daje::Database::Model::Login->new(
            db => $self->db
        )->check_creds(
            $claim->{mail}, $claim->{password}
        );
    } catch($e) {
        say $e;
    };

    return $login;
}

1;

