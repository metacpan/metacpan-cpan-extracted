# ---------------------------------------------------------------------------
# Plumbing — tell the Python SDK to use ws:// against the mock instead of wss://
# ---------------------------------------------------------------------------


def _ws_patch_uri(mock_relay_server: MockRelayServer):
    """Return a contextmanager that overrides the SDK's wss://host URI to ws://host:port.

    The Python client builds its URI as ``f"wss://{self.host}"`` inside
    ``RelayClient.connect``. We monkeypatch ``websockets.connect`` (the
    function the client actually calls) so it ignores the wss URI it was
    given and dials our local ws:// instead.
    """
    target_url = mock_relay_server.ws_url
    real_connect = __import__("websockets").connect

    def proxy(uri, *args, **kwargs):
        # Ignore any URI the SDK passed, dial the mock instead.
        return real_connect(target_url, *args, **kwargs)

    return patch("signalwire.relay.client.websockets.connect", side_effect=proxy)


def _http_journal(mock_relay_server: MockRelayServer) -> list[dict[str, Any]]:
    return requests.get(f"{mock_relay_server.http_url}/__mock__/journal", timeout=5).json()


def _arm_dial(mock_relay_server: MockRelayServer, **kwargs) -> None:
    requests.post(
        f"{mock_relay_server.http_url}/__mock__/scenarios/dial",
        json=kwargs,
        timeout=5,
    )


def _arm_method(mock_relay_server: MockRelayServer, method: str, events: list[dict[str, Any]]) -> None:
    requests.post(
        f"{mock_relay_server.http_url}/__mock__/scenarios/{method}",
        json=events,
        timeout=5,
    )


