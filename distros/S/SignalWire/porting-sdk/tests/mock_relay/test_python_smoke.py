def test_inbound_call_via_push(mock_relay_server: MockRelayServer) -> None:
    """End-to-end: HTTP push of an inbound call → SDK's on_call handler fires
    → SDK sends ``calling.answer`` → mock journals the answer."""
    handler_called = asyncio.Event()
    seen: dict[str, Any] = {}

    async def go():
        with _ws_patch_uri(mock_relay_server):
            client = RelayClient(
                project="inbound-p",
                token="inbound-t",
                host=mock_relay_server.relay_host,
                contexts=["default"],
            )

            @client.on_call
            async def on_call(call):
                seen["call_id"] = call.call_id
                seen["direction"] = call.direction
                # The Python Call surface exposes ``device`` as the raw dict
                # the server sent; from/to live inside its ``params``.
                seen["from"] = (call.device or {}).get("params", {}).get("from_number")
                seen["to"] = (call.device or {}).get("params", {}).get("to_number")
                seen["state_at_entry"] = call.state
                # SDK answers the inbound call; mock will journal a calling.answer.
                await call.answer()
                seen["state_after_answer"] = call.state
                handler_called.set()

            try:
                await client.connect()
                # Push an inbound call via the HTTP control plane.
                await asyncio.to_thread(
                    lambda: requests.post(
                        f"{mock_relay_server.http_url}/__mock__/inbound_call",
                        json={
                            "call_id": "smoke-inbound-1",
                            "from_number": "+15551110000",
                            "to_number": "+15552220000",
                            "context": "default",
                            "auto_states": ["created"],
                        },
                        timeout=5,
                    )
                )
                # Wait for the handler to fire.
                await asyncio.wait_for(handler_called.wait(), timeout=5)
                # Give the calling.answer round trip a moment to land in the journal.
                await asyncio.sleep(0.1)
            finally:
                await client.disconnect()

    asyncio.run(go())

    # Handler observed the right call.
    assert seen["call_id"] == "smoke-inbound-1"
    assert seen["direction"] == "inbound"
    assert seen["from"] == "+15551110000"
    assert seen["to"] == "+15552220000"
    assert seen["state_at_entry"] == "created"

    # Mock journal recorded the SDK's outbound calling.answer.
    j = _http_journal(mock_relay_server)
    answers = [
        e for e in j
        if e["direction"] == "recv" and e["method"] == "calling.answer"
    ]
    assert answers, f"no calling.answer in journal: methods={[e['method'] for e in j]}"
    p = answers[-1]["frame"]["params"]
    assert p["call_id"] == "smoke-inbound-1"


def test_scripted_inbound_flow(mock_relay_server: MockRelayServer) -> None:
    """Full scripted flow:
    1. push calling.call.receive (state=created)
    2. SDK's on_call handler fires
    3. SDK sends calling.answer
    4. push calling.call.state (state=answered)
    5. SDK's on_answered fires
    6. SDK sends calling.play
    7. push calling.call.play (state=playing → finished)
    8. play action resolves"""
    answered_event = asyncio.Event()
    play_completed = asyncio.Event()
    seen: dict[str, Any] = {}

    async def go():
        with _ws_patch_uri(mock_relay_server):
            client = RelayClient(
                project="flow-p",
                token="flow-t",
                host=mock_relay_server.relay_host,
                contexts=["default"],
            )

            @client.on_call
            async def on_call(call):
                seen["call_id"] = call.call_id
                # Answer to advance to "answered".
                await call.answer()

                # Wait until the scripted ``answered`` push lands.
                async def watch_state():
                    while True:
                        if call.state == "answered":
                            return
                        await asyncio.sleep(0.05)

                await asyncio.wait_for(watch_state(), timeout=5)
                seen["state_after_answer_push"] = call.state
                answered_event.set()

                # Now drive a play. The mock's default scenario for calling.play
                # synthesises a 200 response; the play_completed event is fired
                # when we receive the scripted "finished" play event below.
                play = await call.play(
                    [{"type": "tts", "params": {"text": "hello"}}]
                )
                seen["play_kind"] = type(play).__name__
                play_completed.set()

            try:
                await client.connect()

                # Stage 1: arm a play scenario so the play() call gets state events.
                await asyncio.to_thread(
                    lambda: requests.post(
                        f"{mock_relay_server.http_url}/__mock__/scenarios/calling.play",
                        json=[
                            {"emit": {"state": "playing"}, "delay_ms": 5},
                            {"emit": {"state": "finished"}, "delay_ms": 5},
                        ],
                        timeout=5,
                    )
                )

                # Stage 2: push the inbound call notification.
                await asyncio.to_thread(
                    lambda: requests.post(
                        f"{mock_relay_server.http_url}/__mock__/inbound_call",
                        json={
                            "call_id": "flow-call-1",
                            "from_number": "+15551110000",
                            "to_number": "+15552220000",
                            "auto_states": ["created"],
                        },
                        timeout=5,
                    )
                )

                # Stage 3: wait briefly for SDK to send calling.answer, then
                # push the ``answered`` state update so the handler advances.
                await asyncio.sleep(0.2)
                await asyncio.to_thread(
                    lambda: requests.post(
                        f"{mock_relay_server.http_url}/__mock__/push",
                        json={
                            "frame": {
                                "jsonrpc": "2.0",
                                "id": "answered-evt",
                                "method": "signalwire.event",
                                "params": {
                                    "event_type": "calling.call.state",
                                    "params": {
                                        "call_id": "flow-call-1",
                                        "node_id": "mock-relay-node-1",
                                        "tag": "",
                                        "call_state": "answered",
                                        "direction": "inbound",
                                        "device": {
                                            "type": "phone",
                                            "params": {
                                                "from_number": "+15551110000",
                                                "to_number": "+15552220000",
                                            },
                                        },
                                    },
                                },
                            }
                        },
                        timeout=5,
                    )
                )

                await asyncio.wait_for(answered_event.wait(), timeout=5)
                await asyncio.wait_for(play_completed.wait(), timeout=5)
            finally:
                await client.disconnect()

    asyncio.run(go())

    assert seen["call_id"] == "flow-call-1"
    assert seen["state_after_answer_push"] == "answered"
    # SDK returned a PlayAction.
    assert seen["play_kind"] == "PlayAction"

    # Journal confirms the full conversation.
    j = _http_journal(mock_relay_server)
    methods_recv = [e["method"] for e in j if e["direction"] == "recv"]
    # Must contain at least: connect, answer, play.
    assert "signalwire.connect" in methods_recv
    assert "calling.answer" in methods_recv
    assert "calling.play" in methods_recv


def test_unauthenticated_connect_is_rejected(mock_relay_server: MockRelayServer) -> None:
    """Empty creds (after env scrubbing) trigger the mock's AUTH_REQUIRED path.

    We send the connect frame manually because the SDK refuses to construct a
    client without creds.
    """
    import json
    import uuid
    import websockets

    async def go():
        ws = await websockets.connect(mock_relay_server.ws_url)
        try:
            req_id = str(uuid.uuid4())
            await ws.send(json.dumps({
                "jsonrpc": "2.0", "id": req_id, "method": "signalwire.connect",
                "params": {
                    "version": {"major": 2, "minor": 0, "revision": 0},
                    "agent": "test/1.0",
                    "authentication": {"project": "", "token": ""},
                },
            }))
            resp = json.loads(await ws.recv())
            assert "error" in resp
            assert resp["error"]["data"]["signalwire_error_code"] == "AUTH_REQUIRED"
        finally:
            await ws.close()

    asyncio.run(go())
