package Mojolicious::Plugin::Fondation::User;
$Mojolicious::Plugin::Fondation::User::VERSION = '0.02';
# ABSTRACT: User management plugin for Fondation

use Mojo::Base 'Mojolicious::Plugin', -signatures;
use Mojo::JSON qw(true);

sub fondation_meta {
    return {
        dependencies => [
            'Fondation::Model::DBIx::Async',
            'Fondation::MigrationDBIx',
            ],
        defaults => {
            title  => 'User Management',
            models => {
                user => {
                    source  => 'User',
                    backend => undef,  # must be set in app config
                },
            },
        },
    };
}

sub register ($self, $app, $conf) {
    # API routes generated by Fondation::OpenAPI.
    # HTML routes provided by Fondation::User::UI.

    return $self;
}

1;

