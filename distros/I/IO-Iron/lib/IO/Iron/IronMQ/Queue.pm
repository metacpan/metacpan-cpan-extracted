	my $connection = $self->{'connection'};
	my %item_body  = ( 'ids' => [ ], );
    my @message_ids;
    foreach my $msg (@messages) {
        CORE::push @{$item_body{'ids'}}, { 'id' => $msg->id(), 'reservation_id' => $msg->reservation_id(), };
