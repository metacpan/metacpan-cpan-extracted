sub history {
	my ($self, %args) = @_;
    my $trello = $self->trello;
	$trello->http_get(
		uri => URI->new(
            $trello->base_uri . 'cards/' . $self->id . '/actions?filter=updateCard:idList,updateList&member=false'
        )
	)->transform(
        done => sub {
            map Net::Async::Trello::CardAction->new(
                %$_,
                board  => $self,
                trello => $trello
            ), @{ $_[0] }
        }
    )
}

sub update {
    my ($self, %args) = @_;
    my $trello = $self->trello;
	$trello->http_put(
		uri => URI->new(
            $trello->base_uri . 'cards/' . $self->id
        ),
        body => \%args
	)
}

1;

