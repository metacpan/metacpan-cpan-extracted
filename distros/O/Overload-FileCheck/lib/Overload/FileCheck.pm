sub _stat_for {
    my ( $type, $opts ) = @_;

    my @stat = ( (0) x STAT_T_MAX );

    # set file type
    if ( defined $type ) {

        # _S_IFMT is used as a protection to do not flip outside the mask
        $stat[ST_MODE] |= ( $type & _S_IFMT );
    }

    # set permission using octal
    if ( defined $opts->{perms} ) {

        # _S_IFMT is used as a protection to do not flip outside the mask
        $stat[ST_MODE] |= ( $opts->{perms} & ~_S_IFMT );
    }

    # deal with UID / GID
    if ( defined $opts->{uid} ) {
        if ( $opts->{uid} =~ qr{^[0-9]+$} ) {
            $stat[ST_UID] = $opts->{uid};
        }
        else {
            my $uid = getpwnam( $opts->{uid} );
            Carp::croak("Unknown user '$opts->{uid}' passed to uid option") unless defined $uid;
            $stat[ST_UID] = $uid;
        }
    }

    if ( defined $opts->{gid} ) {
        if ( $opts->{gid} =~ qr{^[0-9]+$} ) {
            $stat[ST_GID] = $opts->{gid};
        }
        else {
            my $gid = getgrnam( $opts->{gid} );
            Carp::croak("Unknown group '$opts->{gid}' passed to gid option") unless defined $gid;
            $stat[ST_GID] = $gid;
        }
    }

    # options that we can simply copy to a slot
    my %name2ix = (
        dev     => ST_DEV,
        ino     => ST_INO,
        nlink   => ST_NLINK,
        rdev    => ST_RDEV,
        size    => ST_SIZE,
        atime   => ST_ATIME,
        mtime   => ST_MTIME,
        ctime   => ST_CTIME,
        blksize => ST_BLKSIZE,
        blocks  => ST_BLOCKS,
    );

    # all valid option names (after normalization: lc + strip st_ prefix)
    my %known_opts = ( perms => 1, uid => 1, gid => 1, map { $_ => 1 } keys %name2ix );

    foreach my $orig_key ( keys %$opts ) {
        my $k = lc($orig_key);
        $k =~ s{^st_}{};

        unless ( $known_opts{$k} ) {
            if ( $k eq 'mode' ) {
                Carp::croak("Unknown option '$orig_key': use 'perms' for permission bits (file type is set automatically)");
            }
            Carp::croak("Unknown option '$orig_key' passed to stat helper");
        }

        next unless defined $name2ix{$k};
        $stat[ $name2ix{$k} ] = $opts->{$orig_key};
    }

    return \@stat;
}

######################################################
### Scope guard for automatic mock cleanup
######################################################

package Overload::FileCheck::Guard;

sub new {
    my ( $class, @checks ) = @_;

    return bless { checks => \@checks, active => 1 }, $class;
}

sub cancel {
    my ($self) = @_;

    $self->{active} = 0;
    return;
}

sub DESTROY {
    my ($self) = @_;

    return unless $self->{active};
    $self->{active} = 0;

    local $@;
    eval { Overload::FileCheck::unmock_file_check( @{ $self->{checks} } ) };
    return;
}

