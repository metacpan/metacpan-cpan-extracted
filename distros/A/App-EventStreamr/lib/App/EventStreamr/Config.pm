has 'log_level'     => ( is => 'ro', default => sub { 'INFO, LOG1' } );
has 'mixer'         => ( 
  is => 'rw', 
  lazy => 1, 
  default => sub { 
    {
      host => '127.0.0.1',
      port => '1234',
    }
  }
);

# TODO: Be more intelligent with devices
