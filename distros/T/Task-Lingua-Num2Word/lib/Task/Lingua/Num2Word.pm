=head1 NAME

Task::Lingua::Num2Word - Install all PetaMem number-to-word conversion modules

=head1 VERSION

version 0.2603270

=head1 DESCRIPTION

Installing this module will pull in the L<Lingua::Num2Word> dispatcher
and all language-specific Lingua::*::Num2Word modules (including variants
named Numbers, Number, Nums2Words, Numeros) maintained by PetaMem.

