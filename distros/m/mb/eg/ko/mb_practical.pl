mb::set_script_encoding('sjis');

# A B C 毛聥陇矛聺聦 矛聽聞锚掳聛 铆聻聢毛聺录锚掳聙毛聜聵 a i u 3锚掳聹 毛聥陇矛聺聦 毛掳聵锚掳聛 锚掳聙铆聝聙矛鹿麓毛聜聵 2锚掳聹.
#   ASCII A B C            : 锚掳聛锚掳聛 1矛聴麓
#   矛聽聞锚掳聛 a(\x82\xA0) i(\x82\xA2) u(\x82\xA4) : 锚掳聛锚掳聛 2矛聴麓
#   毛掳聵锚掳聛 ka(\xB6) ki(\xB7)                   : 锚掳聛锚掳聛 1矛聴麓
$line   = "ABC\x82\xA0\x82\xA2\x82\xA4\xB6\xB7";
$budget = 7;   # 7 铆聭聹矛聥聹 矛聴麓毛隆聹 矛聻聬毛娄聞

# 毛卢赂矛聻聬 锚虏陆锚鲁聞矛聴聬 矛聲聢矛聽聞铆聲聵锚鲁聽 铆聫颅矛聺聞 矛聺赂矛聥聺铆聲聵毛聤聰 矛聻聬毛楼麓锚赂掳.
$out = '';
$col = 0;
for $ch (mb::split('', $line)) {
    $w = length($ch);        # 1毛掳聰矛聺麓铆聤赂 -> 1矛聴麓, 2毛掳聰矛聺麓铆聤赂 -> 2矛聴麓
    last if $col + $w > $budget;
    $out .= $ch;
    $col += $w;
}

# ABC (3矛聴麓) + a (2) + i (2) = 7矛聴麓; u毛聤聰 毛聞聵矛鲁聬矛聞聹 毛虏聞毛聽陇矛搂聙毛炉i掳毛隆聹 毛搂聢矛搂聙毛搂聣
