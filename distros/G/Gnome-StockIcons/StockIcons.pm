=head1 NAME

Gnome::StockIcons - Gnome 1.4 stock icons in XPM format

=head1 SYNOPSIS

 use Gtk;
 use Gnome::StockIcons;

 my ( $pixmap, $mask ) = Gtk::Gdk::Pixmap->create_from_xpm_d( $window->window,
                                                        $style,
                                                        @stock_help );

=head1 DESCRIPTION

These are the Gnome 1.4 stock icons. The original PNG files are in gnome-libs-1.4.1.2.90/libgnomeui/pixmaps. All names are identical but '-' is replaced by '_'. Currenlty, this only affects C<calculator-font.png> (@calculator_font).

The module does not strictly speaking require Gtk, but it's not much use without.

