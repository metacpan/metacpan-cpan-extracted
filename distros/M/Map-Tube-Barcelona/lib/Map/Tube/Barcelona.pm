=head1 DESCRIPTION

It currently provides functionality to find the shortest  route between  the  two
given stations. It covers the following Barcelona Metro Lines:

=over 2

=item * L<L1 Line|Map::Tube::Barcelona::Line::L1>

=item * L<L2 Line|Map::Tube::Barcelona::Line::L2>

=item * L<L3 Line|Map::Tube::Barcelona::Line::L3>

=item * L<L4 Line|Map::Tube::Barcelona::Line::L4>

=item * L<L5 Line|Map::Tube::Barcelona::Line::L5>

=item * L<L6 Line|Map::Tube::Barcelona::Line::L6>

=item * L<L7 Line|Map::Tube::Barcelona::Line::L7>

=item * L<L8 Line|Map::Tube::Barcelona::Line::L8>

=item * L<L9 Line|Map::Tube::Barcelona::Line::L9>

=item * L<L10 Line|Map::Tube::Barcelona::Line::L10>

=item * L<L11 Line|Map::Tube::Barcelona::Line::L11>

=back

There is a very handy tool provided by L<Map::Tube::CLI> called C<map-tube> which
exposes the  map  (and all other maps) from command line. Please refer to its pod
document for more details.

=head1 CONSTRUCTOR

The constructor DO NOT expects parameters.This setup the default node definitions.

    use strict; use warnings;
    use Map::Tube::Barcelona;

    my $metro = Map::Tube::Barcelona->new;

=head1 METHODS

=head2 get_shortest_route($from, $to)

It expects C<$from> and C<$to> station name, required param. It returns an object
of type L<Map::Tube::Route>. On error it throws exception of type L<Map::Tube::Exception>.

    use strict; use warnings;
    use Map::Tube::Barcelona;

    my $metro = Map::Tube::Barcelona->new;
    my $route = $metro->get_shortest_route('Roquetes', 'Maragall');

    print "Route: $route\n";

=head2 as_image($line_name)

It expects the plugin  L<Map::Tube::Plugin::Graph> to be  installed. Returns line
image  as  base64  encoded string if C<$line_name> passed in otherwise it returns
base64 encoded string of the entire map.

    use strict; use warnings;
    use MIME::Base64;
    use Map::Tube::Barcelona;

    my $metro = Map::Tube::Barcelona->new;
    my $map   = $metro->name;
    open(my $IMAGE, ">", "$map.png")
       or die "ERROR: Can't open [$map.png]: $!";
    binmode($IMAGE);
    print $IMAGE decode_base64($metro->as_image);
    close($IMAGE);

The L<Barcelona Metro Map|https://raw.githubusercontent.com/manwar/Map-Tube-Barcelona/master/maps/Barcelona-Metro.png>
map as generated by plugin L<Map::Tube::Plugin::Graph>.

