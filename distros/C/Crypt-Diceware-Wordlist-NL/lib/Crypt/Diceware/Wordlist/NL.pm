## no critic (ProhibitPackageVars Capitalization)
our @Words = ();
## use critic
while ( my $wordlist_line = <DATA> ) {
    if (
        $wordlist_line =~ s{[1-6]{5}\s+(?<word>[[:lower:]]+)}{$+{'word'}}gimsx )
    {
        push @Words, $wordlist_line;
    }
}

1;

