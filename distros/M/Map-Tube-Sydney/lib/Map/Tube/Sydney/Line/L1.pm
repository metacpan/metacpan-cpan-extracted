=head1 NAME

Map::Tube::Sydney::Line::L1 - Sydney Rail Map: L1 Line.

=head1 VERSION

Version 1.00

=head1 DESCRIPTION

Sydney Light Rail Map: L1 Line.

    +-------------------------+------------------------------------------+
    | Station Name            | Connected To                             |
    +-------------------------+------------------------------------------+
    | Central Grand Concourse | Capitol Square                           |
    | Capitol Square          | Central Grand Concourse, Paddy's Markets |
    | Paddy's Markets         | Capitol Square, Exhibition Centre        |
    | Exhibition Centre       | Paddy's Markets, Convention Centre       |
    | Convention Centre       | Exhibition Centre, Pyrmont Bay           |
    | Pyrmont Bay             | Convention Centre, The Star              |
    | The Star                | Pyrmont Bay, John Street Square          |
    | John Street Square      | The Star, Fish Market                    |
    | Fish Market             | John Street Square, Wentworth Park       |
    | Wentworth Park          | Fish Market, Glebe                       |
    | Glebe                   | Wentworth Park, Jubilee Park             |
    | Jubilee Park            | Glebe, Rozelle Bay                       |
    | Rozelle Bay             | Jubilee Park, Lilyfield                  |
    | Lilyfield               | Rozelle Bay, Leichhardt North            |
    | Leichhardt North        | Lilyfield, Hawthorne                     |
    | Hawthorne               | Leichhardt North, Marion                 |
    | Marion                  | Hawthorne, Taverners Hill                |
    | Taverners Hill          | Marion, Lewisham West                    |
    | Lewisham West           | Taverners Hill, Waratah Mills            |
    | Waratah Mills           | Lewisham West, Arlington                 |
    | Arlington               | Waratah Mills, Dulwich Grove             |
    | Dulwich Grove           | Arlington, Dulwich Hill                  |
    | Dulwich Hill            | Dulwich Grove                            |
    +-------------------------+------------------------------------------+

=head1 NOTE

=over 2

