=head1 NAME

Task::Labyrinth::Plugin::Music

=head1 SYNOPSIS

  cpan Task::Labyrinth::Plugin::Music

  cpanp -i Task::Labyrinth::Plugin::Music

  cpanm Task::Labyrinth::Plugin::Music

=head1 DESCRIPTION

This tasks installs all L<Labyrinth> modules require to create a music website
built on the Labyrinth Web Framework.

=head1 NAME

Task::Labyrinth::Plugin::Music - All music related Labyrinth modules all at once

=head1 CONTENT

L<Labyrinth> - Labyrinth Website Management Framework

L<Labyrinth::Plugin::Articles::Lyrics> - Lyrics plugin for the Labyrinth framework

L<Labyrinth::Plugin::Articles::Profiles> - Profiles plugin for the Labyrinth framework

L<Labyrinth::Plugin::Core> - Labyrinth Website Management Framework Core Plugins

L<Labyrinth::Plugin::Event> - Event plugin for the Labyrinth framework

L<Labyrinth::Plugin::Event::Types> - Event Type handler for the Labyrinth framework

L<Labyrinth::Plugin::Event::Venues> - Venues administration for the Labyrinth framework

L<Labyrinth::Plugin::MP3s> - MP3 plugin for the Labyrinth framework

L<Labyrinth::Plugin::Release> - Release plugin for the Labyrinth framework

