package Web::PerlDistSite::MenuItem::Homepage;

our $VERSION = '0.001011';

use Moo;
use Web::PerlDistSite::Common -lexical, -all;
use Colouring::In;
use Data::Section -setup;

extends 'Web::PerlDistSite::MenuItem::MarkdownFile';

has 'animation' => (
	is       => 'ro',
	isa      => Enum[ qw/ waves1 waves2 swirl1 attract1 circles1 / ],
	predicate => true,
);

has 'hero_options' => (
	is       => 'ro',
	isa      => HashRef,
	default  => sub { {} },
);

has 'banner' => (
	is       => 'ro',
	isa      => Str,
	predicate => true,
);

has 'banner_fixation' => (
	is       => 'ro',
	isa      => Str,
	default  => 'fixed',
);

has [ 'banner_position_x', 'banner_position_y' ] => (
	is       => 'ro',
	isa      => Str,
	default  => 'center',
);

has '+source' => (
	is       => 'ro',
	default  => '_pages/index.md',
);

has '+name' => (
	is       => 'ro',
	default  => 'index',
);

has '+title' => (
	is       => 'ro',
	default  => 'Home',
);

sub body_class {
	return 'homepage from-markdown';
}

sub page_title ( $self ) {
	return sprintf( '%s: %s', $self->project->name, $self->project->abstract );
}

sub extra_top ( $self ) {
	my @extra;
	if ( $self->has_animation ) {
		my $method = "compile_animation_" . $self->animation;
		push @extra, $self->$method;
	}
	elsif ( $self->has_banner ) {
		push @extra, $self->compile_banner();
	}
	return @extra;
}

sub compile_banner ( $self ) {
	my $template = $self->section_data( 'banner' );
	my $code = $self->_fill_in(
		$$template,
		{
			title     => $self->project->name,
			abstract  => $self->project->abstract_html,
			banner    => $self->banner,
			fixation  => $self->banner_fixation,
			pos_x     => $self->banner_position_x,
			pos_y     => $self->banner_position_y,
			$self->hero_options->%*,
		}
	);
	HTML::HTML5::Parser->new->parse_balanced_chunk( $code );
}

sub compile_animation_waves1 ( $self ) {
	my $colour   = Colouring::In->new( $self->project->theme->{'primary'} );
	my $template = $self->section_data( 'waves1' );
	my $code = $self->_fill_in(
		$$template,
		{
			colour1   => $colour->darken('10%'),
			colour2   => $colour->darken('20%'),
			title     => $self->project->name,
			abstract  => $self->project->abstract_html,
			$self->hero_options->%*,
		}
	);
	HTML::HTML5::Parser->new->parse_balanced_chunk( $code );
}

sub compile_animation_waves2 ( $self ) {
	my $colour   = Colouring::In->new( $self->project->theme->{'primary'} );
	my $template = $self->section_data( 'waves2' );
	my $code = $self->_fill_in(
		$$template,
		{
			colour1   => $colour->lighten('10%'),
			colour2   => $colour->lighten('20%'),
			colour3   => $colour->lighten('30%'),
			colour4   => $colour->darken('20%'),
			colour5   => $colour,
			title     => $self->project->name,
			abstract  => $self->project->abstract_html,
			$self->hero_options->%*,
		}
	);
	HTML::HTML5::Parser->new->parse_balanced_chunk( $code );
}

sub compile_animation_swirl1 ( $self ) {
	my $colour1  = Colouring::In->new( $self->project->theme->{'primary'} );
	my $colour2  = Colouring::In->new( $self->project->theme->{'dark'} );
	my $template = $self->section_data( 'swirl1' );
	my $code = $self->_fill_in(
		$$template,
		{
			colour1   => $colour1,
			colour2   => $colour1->mix( $colour2 ),
			colour3   => $colour2,
			title     => $self->project->name,
			abstract  => $self->project->abstract_html,
			$self->hero_options->%*,
		}
	);
	HTML::HTML5::Parser->new->parse_balanced_chunk( $code );
}

sub compile_animation_attract1 ( $self ) {
	my $colour1  = Colouring::In->new( $self->project->theme->{'dark'} );
	my $template = $self->section_data( 'attract1' );
	my $code = $self->_fill_in(
		$$template,
		{
			colour1   => $colour1,
			title     => $self->project->name,
			abstract  => $self->project->abstract_html,
			$self->hero_options->%*,
		}
	);
	HTML::HTML5::Parser->new->parse_balanced_chunk( $code );
}

sub compile_animation_circles1 ( $self ) {
	my $dark     = Colouring::In->new( $self->project->theme->{'dark'} );
	my $primary  = Colouring::In->new( $self->project->theme->{'primary'} );
	my ( $hue, $saturation ) = ( $primary->toHSL =~ /hsl\((.+),(.+)\%,(.+)\%\)/ );
	my $template = $self->section_data( 'circles1' );
	my $code = $self->_fill_in(
		$$template,
		{
			colour1   => $dark,
			colour2   => $dark->lighten( '20%' ),
			hue       => $hue,
			saturation=> $saturation,
			title     => $self->project->name,
			abstract  => $self->project->abstract_html,
			$self->hero_options->%*,
		}
	);
	HTML::HTML5::Parser->new->parse_balanced_chunk( $code );
}

sub _fill_in ( $self, $template, $data ) {
	for my $key ( keys $data->%* ) {
		$template =~ s/\{\{\s*\$\Q$key\E\s*\}\}/$data->{$key}/ge;
	}
	return $template;
}

