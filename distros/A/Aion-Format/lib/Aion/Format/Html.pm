sub split_on_pages(@) {
	my ($html, $symbols_on_page, $by) = @_;

	# –ù–∞ –∫–∞–∫–æ–µ —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ —Å—Ç—Ä–∞–Ω–∏—Ü–∞ –º–æ–∂–µ—Ç –±—ã—Ç—å –±–æ–ª—å—à–µ
	$by //= $symbols_on_page / 3 < 1000 ? int($symbols_on_page / 3): 1000;
	my $max = $symbols_on_page + $by;

	my @pages;		# –º–∞—Å—Å–∏–≤ —Å—Ç—Ä–∞–Ω–∏—Ü
	my @page;		# –º–∞—Å—Å–∏–≤ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —Ç–µ–∫—Å—Ç–∞ –∏ —Ç–µ–≥–æ–≤ —Ç–µ–∫—É—â–µ–π —Å—Ç—Ä–∞–Ω–∏—Ü—ã
	my $c = 0;		# –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–∏–º–≤–æ–ª–æ–≤ –≤ —Ç–µ–∫—É—â–µ–π —Å—Ç—Ä–∞–Ω–∏—Ü–µ
	my $i_page = 0;	# –∏–Ω–¥–µ–∫—Å —ç–ª–µ–º–µ–Ω—Ç–∞ @page –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–∏–≤—ã—Å–∏–ª —Ä–∞–∑–º–µ—Ä —Å—Ç—Ä–∞–Ω–∏—Ü—ã
	my $c_page = 0;	# –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–∏–º–≤–æ–ª–æ–≤ @page –ø–æ $i_page
	my $is_proposal = 0;			# –í –∫–æ–Ω—Ü–µ —Ç–µ–∫—É—â–µ–π —Å—Ç—Ä–∞–Ω–∏—Ü—ã –æ–±–Ω–∞—Ä—É–∂–µ–Ω –∫–æ–Ω–µ—Ü –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è
	my $re_proposal = qr/[.?!‚Ä¶]/;
	my @S;			# –º–∞—Å—Å–∏–≤ –æ—Ç–∫—Ä—ã–≤–∞—é—â–∏—Ö —Ç–µ–≥–æ–≤ [tag, '<tag ...>']

	# –§—É–Ω–∫—Ü–∏—è —Ñ–∏–∫—Å–∏—Ä—É–µ—Ç —Å—Ç—Ä–∞–Ω–∏—Ü—É –∏ —Å–±—Ä–∞—Å—ã–≤–∞–µ—Ç —Å—á—ë—Ç—á–∏–∫–∏
	my $make_page = sub {
		push @pages, join "", @page, map { "</$_->[0]>" } reverse @S;
		$i_page = $c = $is_proposal = 0;
		@page = map $_->[1], @S;
	};
	
	for(grep length, split m{(
		<[a-z] [^<>]* >
		| </ \s* [a-z]\w* \s* >
		| &(?: [a-z]\w* | \# \d+ | \#x[0-9a-f]+ ) ;?
		| \n 		# –ê–±–∑–∞—Ü
		| $re_proposal+	# –ü—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ
		| \b		# –°–ª–æ–≤–æ
	)}xiu, $html) {

		if(/^&/) {$c++} 	# html-—Å–∏–º–≤–æ–ª
		elsif(/^<\/\s*([a-z]\w*)/) { # –∑–∞–∫—Ä—ã–≤–∞—é—â–∏–π —Ç–µ–≥
			my $tag = lc $1;
			eval { out_tag @S, $tag };
			next if $@;
			# </p> –ø—Ä–µ–≤—Ä–∞—â–∞–µ–º –≤ <p></p>
			$_ = "<p></p>" if $tag eq "p";
		}
		elsif(/^<([a-z]\w*)/) { in_tag @S, lc $1, $_ }	# —Ç–µ–≥
		else {$c += length}	# —Ç–µ–∫—Å—Ç

		push @page, $_; # –Ω–∞–∫–∞–ø–ª–∏–≤–∞–µ–º —Å–∏–º–≤–æ–ª—ã –≤ –º–∞—Å—Å–∏–≤–µ @page

		next if $c < $symbols_on_page;	# —Å—Ç—Ä–∞–Ω–∏—Ü—É –Ω–µ –Ω–∞–±—Ä–∞–ª–∏ - —Ç–æ–≥–¥–∞ –Ω–∞ next

		$c_page = $c, $i_page = @page if !$i_page;

		# –ü—Ä–æ—Å–º–∞—Ç—Ä–∏–≤–∞–µ–º –≤–ø–µ—Ä—ë–¥ –ø–æ–∫–∞ –Ω–µ –Ω–∞–π–¥—ë–º –≥—Ä–∞–Ω–∏—Ü—É –∏–ª–∏ –Ω–µ –¥–æ—Å—Ç–∏–≥–Ω–µ–º –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏—è
		if(/^\n/) { $make_page->() } 	# –ê–±–∑–∞—Ü
		elsif(!$is_proposal && /^$re_proposal/) { $i_page = @page; $c_page = $c; $is_proposal = 1 }
		elsif($c >= $max) {
			# –ï—Å–ª–∏ —Å–ª–µ–¥—É—é—â–∏–π –∑–∞ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ–º –∏–ª–∏ —Å–ª–æ–≤–æ–º —ç–ª–µ–º–µ–Ω—Ç - –ø—Ä–æ–±–µ–ª—ã, —Ç–æ –¥–æ–±–∞–≤–ª—è–µ–º –∏—Ö –∫ —Å—Ç—Ä–∞–Ω–∏—Ü–µ
			$c_page -= length $page[$i_page++] if $page[$i_page] =~ /^\s/;
			my @x = splice @page, $i_page;
			$make_page->();
			push @page, @x;
			$c -= $c_page;
		}
	}

	$make_page->() if @page;

	# –ï—Å–ª–∏ —Ä–∞–∑–º–µ—Ä –ø–æ—Å–ª–µ–¥–Ω–µ–π —Å—Ç—Ä–∞–Ω–∏—Ü—ã –º–µ–Ω—å—à–µ —á–µ–º 2/3, —Ç–æ –¥–æ–±–∞–≤–ª—è–µ–º –µ—ë –∫ –ø—Ä–µ–¥–ø–æ—Å–ª–µ–¥–Ω–µ–π
	$pages[$#pages - 1] .= pop @pages if @pages > 1 and length($pages[$#pages]) < $symbols_on_page * 2 / 3;

	#my $len = 0; $len += length for @pages;
	#die "–°—É–º–º–∞—Ä–Ω—ã–π —Ä–∞–∑–º–µ—Ä —Å—Ç—Ä–∞–Ω–∏—Ü –Ω–µ –∏–∑–º–µ–Ω–∏–ª—Å—è: " . length($html) . " == $len pages=" . @pages . " ->\n\n$html" if $len == length $html;

	my ($end1) = $html =~ m!([^<>\s]{1,13})\s*(</?\w[^<>]*>\s*)*$!a;
	my ($end2) = $pages[$#pages] =~ m!([^<>\s]{1,13})\s*(</?\w[^<>]*>\s*)*$!a;
	die "–ö–æ–Ω—Ü—ã —Ç–µ–∫—Å—Ç–∞ –∏ –ø–æ—Å–ª–µ–¥–Ω–µ–π —Å—Ç—Ä–∞–Ω–∏—Ü—ã –Ω–µ —Å—Ö–æ–¥—è—Ç—Å—è! `$end1` <> `$end2`" if $end1 ne $end2;

	return @pages;
}

