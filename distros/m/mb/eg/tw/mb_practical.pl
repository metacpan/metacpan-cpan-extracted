use strict;
use vars qw($line $budget $out $col $ch $w $aiu $octet_cut $char_cut);

use FindBin;
use lib "$FindBin::Bin/../../lib";
use mb;
mb::set_script_encoding('sjis');

# A B C 莽聞露氓戮聦盲赂聣氓聙聥氓聟篓氓陆垄氓鹿鲁氓聛聡氓聬聧 a i u 莽聞露氓戮聦氓聟漏氓聙聥氓聧聤氓陆垄莽聣聡氓聛聡氓聬聧茫聙聜
#   ASCII A B C            : 忙炉聫氓聙聥 1 忙卢聞
#   氓聟篓氓陆垄 a(\x82\xA0) i(\x82\xA2) u(\x82\xA4) : 忙炉聫氓聙聥 2 忙卢聞
#   氓聧聤氓陆垄 ka(\xB6) ki(\xB7)                   : 忙炉聫氓聙聥 1 忙卢聞
$line   = "ABC\x82\xA0\x82\xA2\x82\xA4\xB6\xB7";
$budget = 7;   # 盲驴庐氓聣陋氓聢掳 7 茅隆炉莽陇潞忙卢聞

# 氓颅聴氓聟聝茅聜聤莽聲聦氓庐聣氓聟篓茫聙聛忙聞聼莽聼楼氓炉卢氓潞娄莽職聞盲驴庐氓聣陋茫聙聜
$out = '';
$col = 0;
for $ch (mb::split('', $line)) {
    $w = length($ch);        # 1 byte -> 1 忙卢聞茂录聦2 byte -> 2 忙卢聞
    last if $col + $w > $budget;
    $out .= $ch;
    $col += $w;
}

# ABC (3 忙卢聞) + a (2) + i (2) = 7 忙卢聞茂录聸u 忙聹聝忙潞垄氓聡潞猫聙聦猫垄芦盲赂聼忙拢聞茂录聦忙聲聟忙聹芦
# 氓掳戮茅聸聶盲陆聧氓聟聝莽碌聞氓颅聴氓聟聝盲驴聺忙聦聛氓庐聦忙聲麓茫聙聜
print "input chars      : ", mb::length($line), "\n";        # 8
print "budget columns   : $budget\n";                        # 7
print "kept chars       : ", mb::length($out), "\n";         # 5
print "used columns     : $col\n";                           # 7
print "kept bytes       : ", length($out), "\n";             # 7

# 氓掳聧氓聝聟茅聸聶盲陆聧氓聟聝莽碌聞猫鲁聡忙聳聶莽職聞氓掳聧忙炉聰茂录職盲禄楼氓聸潞氓庐職盲陆聧氓聟聝莽碌聞忙聲赂氓聢聡氓聣虏
# 氓聫炉猫聝陆猫聬陆氓聹篓氓颅聴氓聟聝氓聟搂茅聝篓茫聙聜氓鹿鲁氓聛聡氓聬聧 a i u 氓聟篓忙聵炉茅聸聶盲陆聧氓聟聝莽碌聞茂录聦忙聲聟
# 氓楼聡忙聲赂 byte 茅聲路氓潞娄猫隆篓莽陇潞氓聢聡氓聣虏忙聣聯忙聳路盲潞聠氓颅聴氓聟聝茂录聸mb::substr 莽赂陆忙聵炉
# 氓聹篓氓颅聴氓聟聝茅聜聤莽聲聦氓聛聹忙颅垄茂录聢茅聙聶猫拢隆忙聵炉氓聛露忙聲赂 byte 茅聲路氓潞娄茂录聣茫聙聜
$aiu       = "\x82\xA0\x82\xA2\x82\xA4";
$octet_cut = substr($aiu, 0, 3);        # a + 盲赂聙氓聙聥忙聡赂莽漏潞莽職聞氓聣聧氓掳聨 byte
$char_cut  = mb::substr($aiu, 0, 1);    # 忙聛掳氓楼陆忙聵炉 a
print "CORE substr 3oct : ", length($octet_cut), " bytes (odd -> split)\n"; # 3
print "mb::substr 1char : ", length($char_cut),  " bytes (whole char)\n";   # 2

exit 0;
