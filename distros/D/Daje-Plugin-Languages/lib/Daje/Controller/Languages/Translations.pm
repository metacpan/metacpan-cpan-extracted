sub translations_list($self) {

    $self->app->log->debug('Daje::Controller::Languages::Translations::translations Starts');
    $self->render_later;
    my ($companies_pkey, $users_pkey) = $self->jwt->companies_users_pkey(
        $self->req->headers->header('X-Token-Check')
    );
    # my $plugin = $self->param('plugin');
    $self->translations->load_translations_p($users_pkey)->then(sub($result) {
        $self->render(json => $result->to_array());
    })->catch(sub($err) {
        $self->app->log->error('Daje::Controller::Languages::Translations::translations ' . $err);
        $self->render
    });

