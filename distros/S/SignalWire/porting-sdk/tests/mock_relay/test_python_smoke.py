"""Smoke test: ``signalwire.relay.RelayClient`` against the mock relay server.

This proves the mock can stand in for the real RELAY for the Python SDK.
For each major surface — connect, execute, dial, send_message, reconnect —
we exercise one call and assert:

1. The SDK call returns a non-error response.
2. The mock journal recorded the inbound frame with the right method.
3. (Where applicable) scripted events from the mock made it through the
   SDK's event dispatch.

Each test is a separate ``def test_*`` so a single namespace failure does
not mask others. When a Python relay test pattern can't be supported by
the mock, the gap is appended to ``MOCK_RELAY_GAPS.md`` and the test is
skipped.
"""

