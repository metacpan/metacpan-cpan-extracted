sub new {

    my ($class) = @_;

    my $self = {
        Code  => 0,
        Left  => 0,
        Freqs => 0,
        MBs   => 0,
        Total => 0,
        Bad   => 0,
    };

    return bless $self, $class;

}

sub take {

    my ($self, $bytes) = @_;

    return TAKE_BAD if $self->{Bad};

    for my $i (0 .. length($bytes) - 1) {
        my $b = ord(substr $bytes, $i, 1) & 0xff;
        if ($self->{Left}) {
            $self->{Code} = ($self->{Code} << 8) | $b;
            $self->{Left}--;
            if (exists $BIG5_FREQS{ $self->{Code} }) {
                $self->{Freqs}++;
            }
            $self->{MBs}++;
            $self->{Total}++;
        } elsif ($b >= 0xa4 && $b <= 0xfe) {
            $self->{Code} = $b;
            $self->{Left} = 1;
        } else {
            $self->{Total}++;
        }
    }

    return TAKE_OK;

}

sub confidence {

    my ($self) = @_;

    if ($self->{Bad} or $self->{MBs} == 0) {
        return 0;
    }

    return $self->{Freqs} / $self->{MBs};

}

sub bad {

    my ($self) = @_;

    return $self->{Bad};

}

