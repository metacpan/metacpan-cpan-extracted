package Bundle::HTMLWikiConverter;

$VERSION = '0.04';

1;

__END__

=head1 NAME

Bundle::HTMLWikiConverter - A bundle to install HTML::WikiConverter and all its dialects

=head1 SYNOPSIS

 perl -MCPAN -e 'install Bundle::HTMLWikiConverter'

=head1 CONTENTS

HTML::WikiConverter 0.55

HTML::WikiConverter::DokuWiki

HTML::WikiConverter::Kwiki

HTML::WikiConverter::Markdown

HTML::WikiConverter::MediaWiki

HTML::WikiConverter::MoinMoin

HTML::WikiConverter::Oddmuse

HTML::WikiConverter::PbWiki

HTML::WikiConverter::PhpWiki

HTML::WikiConverter::PmWiki

HTML::WikiConverter::SnipSnap

HTML::WikiConverter::TikiWiki

HTML::WikiConverter::UseMod

HTML::WikiConverter::WakkaWiki

HTML::WikiConverter::WikkaWiki

=head1 DESCRIPTION

This bundle includes the L<HTML::WikiConverter> distribution as well
as all available dialect modules needed to convert HTML into a variety
of wiki markups.

