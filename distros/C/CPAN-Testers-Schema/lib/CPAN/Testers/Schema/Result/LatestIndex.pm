=head1 NAME

CPAN::Testers::Schema::Result::LatestIndex - A cache of the latest version of a dist by author

=head1 VERSION

version 0.028

=head1 SYNOPSIS

    my $ix = $schema->resultset( 'LatestIndex' )->find({
        dist => 'My-Dist',
        author => 'PREACTION',
    });

    $schema->resultset( 'LatestIndex' )->find_or_create({
