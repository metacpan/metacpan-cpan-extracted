=head1 NAME

Task::Business::AU - A Task module for Australian Businesses

=head1 DESCRIPTION

There is a gradual awareness in Australia that Open Source is good
for business. But the modules that have been created so far are
somewhat ad-hoc, and scattered around.

The goal for B<Task::Business::AU> is to provide a focus for
the development of Australia-specific Business:: modules, and
provide a single C< cpan Task::Business::AU > command that can be
used to install all the modules in one hit.

