sub new {

    my ($class) = @_;

    my $self = {
        Code  => 0,
        Left  => 0,
        Freqs => 0,
        MBs   => 0,
        Total => 0,
        Bad   => 0,
    };

    return bless $self, $class;

}

sub take {

    my ($self, $bytes) = @_;

    return TAKE_BAD if $self->{Bad};

    for my $i (0 .. length($bytes) - 1) {
        my $b = ord(substr $bytes, $i, 1) & 0xff;
        if ($self->{Left}) {
            if ($b >= 0xa1 && $b <= 0xfe) {
                $self->{Code} = ($self->{Code} << 8) | $b;
                $self->{Left}--;
                if (exists $GB2312_FREQS{ $self->{Code} }) {
                    $self->{Freqs}++;
                }
                $self->{MBs}++;
                $self->{Total}++;
                $self->{Code} = 0;
            } else {
                $self->{Bad} = 1;
                return TAKE_BAD;
            }
        } elsif ($b >= 0xa1 && $b <= 0xfe) {
            $self->{Code} = $b;
            $self->{Left} = 1;
        } elsif ($b & 0x80) {
            $self->{Bad} = 1;
            return TAKE_BAD;
        } else {
            $self->{Total}++;
        }
    }

    return TAKE_OK;

}

sub confidence {

    my ($self) = @_;

    if ($self->{Bad} or $self->{MBs} == 0) {
        return 0;
    }

    return $self->{Freqs} / $self->{MBs};

}

sub bad {

    my ($self) = @_;

    return $self->{Bad};

}

