use strict;
use vars qw($aiu @byte @char $csv @field);

use FindBin;
use lib "$FindBin::Bin/../../lib";
use mb;
mb::set_script_encoding('sjis');

# Shift_JIS 铆聻聢毛聺录锚掳聙毛聜聵 3锚掳聹: a(\x82\xA0) i(\x82\xA2) u(\x82\xA4).
$aiu = "\x82\xA0\x82\xA2\x82\xA4";

# CORE split(//, ...)毛聤聰 毛掳聰矛聺麓铆聤赂毛楼录 毛鲁赂毛聥陇: 矛聴卢锚赂掳矛聞聹毛聤聰 6锚掳聹.
@byte = split(//, $aiu);
print "CORE split(//)   : ", scalar(@byte), " pieces (bytes)\n";   # 6

# mb::split('', ...)毛聤聰 毛卢赂矛聻聬毛楼录 毛鲁赂毛聥陇: 3锚掳聹.
@char = mb::split('', $aiu);
print "mb::split('')    : ", scalar(@char), " pieces (chars)\n";   # 3

# MBCS 锚碌卢毛露聞矛聻聬毛隆聹 毛聜聵毛聢聞锚赂掳. 锚碌卢毛露聞矛聻聬毛聤聰 铆聻聢毛聺录锚掳聙毛聜聵 a
# (\x82\xA0)矛聺麓毛聥陇; mb::split毛聤聰 锚路赂锚虏聝矛聺聞 铆聲聹 毛卢赂矛聻聬毛隆聹 矛聺录矛鹿聵矛聥聹铆聜陇毛漏掳 \x82毛聜聵
# \xA0 毛掳聰矛聺麓铆聤赂锚掳聙 矛聳麓毛聰聰矛聴聬 毛聜聵铆聝聙毛聜聵毛聯聽 锚卤掳锚赂掳矛聴聰 毛搂聻矛露聰矛搂聙 矛聲聤毛聤聰毛聥陇.
#     A a B a C  ->  铆聲聞毛聯聹: A, B, C
$csv   = "A\x82\xA0B\x82\xA0C";
@field = mb::split("\x82\xA0", $csv);
print "fields on MBCS   : ", scalar(@field), " (", join(',', @field), ")\n"; # 3 (A,B,C)

# mb::split矛聺聵 毛娄卢矛聤陇铆聤赂 毛卢赂毛搂楼矛聹录毛隆聹 毛卢赂矛聻聬 矛聢聵 矛聞赂锚赂掳(chars() 毛聫聞矛職掳毛炉i掳 锚掳聶矛聺聦).
{
    local($^W) = undef;
    print "character count  : ", scalar(mb::split('', $aiu)), "\n";   # 3
}

