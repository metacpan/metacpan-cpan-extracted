sub new {

    my ($class) = @_;

    my $self = {
        Code  => 0,
        Left  => 0,
        Set   => 0,
        Freqs => 0,
        MBs   => 0,
        Total => 0,
        Bad   => 0,
    };

    return bless $self, $class;

}

sub take {

    my ($self, $bytes) = @_;

    return TAKE_BAD if $self->{Bad};

    for my $i (0 .. length($bytes) - 1) {
        my $b = ord(substr $bytes, $i, 1) & 0xff;
        if ($self->{Set} == 3) {
            if ($b < 0xa1 or $b > 0xfe) {
                $self->{Bad} = 1;
                return TAKE_BAD;
            }
            $self->{Code} = ($self->{Code} << 8) | $b;
            $self->{Left}--;
            if ($self->{Left} == 0) {
                if (exists $EUCJP_FREQS{ $self->{Code} }) {
                    $self->{Freqs}++;
                }
                $self->{Total}++;
                $self->{MBs}++;
                $self->{Code} = 0;
                $self->{Set} = 0;
            }
        } elsif ($self->{Set} == 2) {
            if ($b < 0xa1 or $b > 0xdf) {
                $self->{Bad} = 1;
                return TAKE_BAD;
            }
            $self->{Code} = ($self->{Code} << 8) | $b;
            $self->{Left}--;
            if (exists $EUCJP_FREQS{ $self->{Code} }) {
                $self->{Freqs}++;
            }
            $self->{Total}++;
            $self->{MBs}++;
            $self->{Code} = 0;
            $self->{Set} = 0;
        } elsif ($self->{Set} == 1) {
            # The range is supposed to be 0xa1-0xfe, but HP-16 allows for
            # lower trailing bytes.
            if ($b < 0x21 or $b > 0xfe) {
                $self->{Bad} = 1;
                return TAKE_BAD;
            }
            $self->{Code} = ($self->{Code} << 8) | $b;
            if (exists $EUCJP_FREQS{ $self->{Code} }) {
                $self->{Freqs}++;
            }
            $self->{Left}--;
            $self->{MBs}++;
            $self->{Total}++;
            $self->{Code} = 0;
            $self->{Set} = 0;
        } elsif ($b == 0x8f) {
            $self->{Set} = 3;
            $self->{Left} = 2;
            $self->{Code} = $b;
        } elsif ($b == 0x8e) {
            $self->{Set} = 2;
            $self->{Left} = 1;
            $self->{Code} = $b;
        } elsif ($b >= 0xa1 && $b <= 0xfe) {
            $self->{Set} = 1;
            $self->{Left} = 1;
            $self->{Code} = $b;
        } elsif ($b & 0x80) {
            $self->{Bad} = 1;
            return TAKE_BAD;
        } else {
            $self->{Total}++;
        }
    }

    return TAKE_OK;

}

sub confidence {

    my ($self) = @_;

    if ($self->{Bad} or $self->{MBs} == 0) {
        return 0;
    }

    return $self->{Freqs} / $self->{MBs};

}

sub bad {

    my ($self) = @_;

    return $self->{Bad};

}

