sub new {

    my ($class) = @_;

    my $self = {
        Code  => 0,
        Left  => 0,
        Freqs => 0,
        MBs   => 0,
        Total => 0,
        Bad   => 0,
    };

    return bless $self, $class;

}

sub take {

    my ($self, $bytes) = @_;

    return TAKE_BAD if $self->{Bad};

    for my $i (0 .. length($bytes) - 1) {
        my $b = ord(substr $bytes, $i, 1) & 0xff;
        if ($self->{Left}) {
            ## So if the leading byte is even, the trailing byte must be in
            ## range 0x9f-0xfc. If the leading byte is odd, is must in range
            ## 0x40-0x9e (but not 0x7f). However, the texts I tested this
            ## on did not follow these rules, so I guess we ignore them :-/.
            #if ($self->{Code} % 2 == 0) {
            #    if ($b >= 0x9f && $b <= 0xfc) {
            #        $self->{Code} = ($self->{Code} << 8) | $b;
            #    } else {
            #        $self->{Bad} = 1;
            #        return TAKE_BAD;
            #    }
            #} else {
            #    if ($b >= 0x40 && $b <= 0x9e && $b != 0x7f) {
            #        $self->{Code} = ($self->{Code} << 8) | $b;
            #    } else {
            #        say $self->{Total};
            #        $self->{Bad} = 1;
            #        return TAKE_BAD;
            #    }
            #}
            $self->{Code} = ($self->{Code} << 8) | $b;
            if (exists $SHIFTJIS_FREQS{ $self->{Code} }) {
                $self->{Freqs}++;
            }
            $self->{MBs}++;
            $self->{Total}++;
            $self->{Left}--;
            $self->{Code} = 0;
        } elsif (($b >= 0x81 && $b <= 0x9f) or ($b >= 0xe0 && $b <= 0xef)) {
            $self->{Code} = $b;
            $self->{Left} = 1;
        # TODO: Does this interfere with ShiftJIS extensions
        } elsif ($b & 0x80) {
            $self->{Bad} = 1;
            return TAKE_BAD;
        } else {
            $self->{Total}++;
        }
    }

    return TAKE_OK;

}

sub confidence {

    my ($self) = @_;

    if ($self->{Bad} or $self->{MBs} == 0) {
        return 0;
    }

    return $self->{Freqs} / $self->{MBs};

}

sub bad {

    my ($self) = @_;

    return $self->{Bad};

}

