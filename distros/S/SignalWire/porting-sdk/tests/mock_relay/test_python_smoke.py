def test_connect_returns_protocol(mock_relay_server: MockRelayServer) -> None:
    """``client.connect()`` succeeds and the journal logs a signalwire.connect."""
    async def go():
        with _ws_patch_uri(mock_relay_server):
            client = RelayClient(
                project="smoke-project",
                token="smoke-token",
                host=mock_relay_server.relay_host,
                contexts=["default"],
            )
            try:
                await client.connect()
                # The SDK stores the issued protocol string.
                assert client.relay_protocol.startswith("signalwire_"), \
                    f"unexpected protocol: {client.relay_protocol!r}"
            finally:
                await client.disconnect()

    asyncio.run(go())

    j = _http_journal(mock_relay_server)
    connect_recv = [e for e in j if e["direction"] == "recv" and e["method"] == "signalwire.connect"]
    assert connect_recv, "no signalwire.connect entry in journal"
    p = connect_recv[-1]["frame"]["params"]
    assert p["authentication"]["project"] == "smoke-project"
    assert p["authentication"]["token"] == "smoke-token"


