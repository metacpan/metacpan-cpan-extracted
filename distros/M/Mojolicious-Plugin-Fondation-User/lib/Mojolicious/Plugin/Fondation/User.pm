=head1 NAME

Mojolicious::Plugin::Fondation::User - User management plugin for Fondation

=head1 VERSION

version 0.02

=head1 SYNOPSIS

  # myapp.conf
  {
      'Fondation' => {
          dependencies => [
              { 'Fondation::Model::DBIx::Async' => {
                  backends => [ main => {
                      dsn          => 'dbi:SQLite:dbname=data/app.db',
                      schema_class => 'MySchema',
                  }],
              }},
              'Fondation::User',
          ],
      },
  }

=head1 DESCRIPTION

L<Mojolicious::Plugin::Fondation::User> provides the C<users> database table
(schema, Result, and ResultSet classes) and a REST controller for CRUD
operations.

Routes are auto-generated by L<Fondation::OpenAPI>. HTML UI is provided by
L<Fondation::User::UI::Bootstrap>.

=head2 Schema

The C<users> table has these columns:

  id          INTEGER PRIMARY KEY AUTOINCREMENT
  username    VARCHAR(100) NOT NULL
  email       VARCHAR(255) NOT NULL
  password    VARCHAR(255) NOT NULL  (hashed with Argon2)
  created_at  DATETIME NOT NULL
  updated_at  DATETIME NOT NULL
  active      INTEGER DEFAULT 1

Passwords are automatically hashed with L<Crypt::Passphrase> (Argon2) via
C<insert()> and C<update()> hooks in the Result class. The hashing runs in
the DBIx::Class::Async worker process, keeping the event loop free.

=head2 Controller

REST endpoints (routes generated by OpenAPI):

  GET    /api/user        list all users
  POST   /api/user        create a user
  GET    /api/user/:id    read a user
  PUT    /api/user/:id    update a user
  DELETE /api/user/:id    delete a user

The C<password> field is never returned in API responses.

=head2 ResultSet extensions

  $c->model('user')->active;           # users with active = 1
  $c->model('user')->created_today;    # users created today
  $c->model('user')->latest;           # latest 10 users

=head2 Translations

Translations for API notifications are shipped in C<share/translations/>.
English and French are included.

