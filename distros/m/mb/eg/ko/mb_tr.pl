use strict;
use vars qw($fw $zenkaku $n $dame $core $cn $safe $sn $keep $out);

use FindBin;
use lib "$FindBin::Bin/../../lib";
use mb;
mb::set_script_encoding('sjis');

# Shift_JIS 矛聽聞锚掳聛 矛聢芦矛聻聬: 0(\x82\x4F) .. 9(\x82\x58).
# SEARCH毛聤聰 10锚掳聹 矛聽聞锚掳聛 矛聢芦矛聻聬毛楼录 铆聲聵毛聜聵矛聰漏 毛聜聵矛聴麓铆聲聵锚鲁聽, REPLACE毛聤聰 US-ASCII
# 毛虏聰矛聹聞 "0-9"矛聺麓毛聥陇(mb::tr锚掳聙 铆聶聲矛聻楼铆聲聵毛聤聰 ASCII 铆聲聵矛聺麓铆聰聢 毛虏聰矛聹聞).
$zenkaku = "\x82\x4F\x82\x50\x82\x51\x82\x52\x82\x53"
         . "\x82\x54\x82\x55\x82\x56\x82\x57\x82\x58";

# 矛聽聞锚掳聛 "1" "3" "6" -> 毛掳聵锚掳聛 "136".
$fw = "\x82\x50\x82\x52\x82\x55";
$n  = mb::tr($fw, $zenkaku, "0-9");
print "full->half count : $n\n";        # 3
print "full->half result: $fw\n";        # 136

# DAMEMOJI 矛聲聢矛聽聞矛聞卤. 毛卢赂矛聻聬矛聴麓矛聺聙 A So(\x83\x5C) B矛聺麓毛聥陇. 矛聴颅矛聤卢毛聻聵矛聥聹 毛掳聰矛聺麓铆聤赂
# \x5C毛楼录 毛聟赂毛娄掳 CORE tr毛聤聰 毛卢赂矛聻聬毛楼录 矛聠聬矛聝聛矛聥聹铆聜陇矛搂聙毛搂聦, ASCII 毛卢赂矛聻聬毛搂聦 毛搂陇铆聲聭铆聲聵毛聤聰
# mb::tr毛聤聰 So毛楼录 矛聵篓矛聽聞铆聲聵锚虏聦 毛聭聰毛聥陇.
$dame = "A\x83\x5CB";

$core = $dame;
$cn   = ($core =~ tr/\x5C/#/);   # CORE tr锚掳聙 So矛聺聵 毛聛聺 毛掳聰矛聺麓铆聤赂矛聴聬 毛搂聻毛聤聰毛聥陇
print "CORE tr on \\x5C  : count=$cn (corrupts DAMEMOJI)\n";   # 1

$safe = $dame;
$sn   = mb::tr($safe, "AB", "ab");
print "mb::tr letters   : count=$sn, DAMEMOJI kept=",
      (substr($safe, 1, 2) eq "\x83\x5C" ? 1 : 0), "\n";       # 2, 1

# /r 铆聲聹矛聽聲矛聻聬: 毛鹿聞铆聦聦锚麓麓矛聽聛, 矛聽聞矛聻聬毛聬聹 毛鲁碌矛聜卢毛鲁赂矛聺聞 毛掳聵铆聶聵铆聲聹毛聥陇.
$keep = "\x82\x50\x82\x51";                 # 矛聽聞锚掳聛 1 2
$out  = mb::tr($keep, $zenkaku, "0-9", "r");
print "/r result        : $out\n";                            # 12
print "/r original kept : ",
      ($keep eq "\x82\x50\x82\x51" ? 1 : 0), "\n";            # 1

exit 0;
