package Rex::Endpoint::HTTP::Interface::System::Base;
   
use strict;
use warnings;

sub new {
   my $that = shift;
   my $proto = ref($that) || $that;
   my $self = { @_ };

   bless($self, $proto);

   return $self;
}

sub set_routes {
   my ($self, $r) = @_;
}

1;
