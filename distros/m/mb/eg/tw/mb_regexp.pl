use strict;
use vars qw($aiu @core @mbcs $dot $range $cap @found);

use FindBin;
use lib "$FindBin::Bin/../../lib";
use mb;
mb::set_script_encoding('sjis');

# Shift_JIS 盲赂聣氓聙聥氓鹿鲁氓聛聡氓聬聧茂录職 a(\x82\xA0) i(\x82\xA2) u(\x82\xA4)茫聙聜
$aiu = "\x82\xA0\x82\xA2\x82\xA4";

# CORE "." 忙聵炉盲赂聙氓聙聥盲陆聧氓聟聝莽碌聞 -- 氓聟颅 byte 氓戮聴氓聟颅忙庐碌茫聙聜
@core = ($aiu =~ /(.)/g);
print "CORE /(.)/g      : ", scalar(@core), " pieces (bytes)\n";   # 6

# mb::qr("(.)") 盲陆驴 "." 忙聢聬莽聜潞盲赂聙氓聙聥忙聲麓氓颅聴氓聟聝 -- 盲赂聣忙庐碌茫聙聜氓聟聢莽路篓猫颅炉
# 盲赂聙忙卢隆茂录聦氓聠聧氓掳聡莽路篓猫颅炉氓楼陆莽職聞忙篓隆氓录聫氓聟搂忙聫聮氓聢掳忙炉聰氓掳聧盲赂颅茫聙聜
$dot  = mb::qr("(.)");
@mbcs = ($aiu =~ /$dot/g);
print "mb::qr /(.)/g    : ", scalar(@mbcs), " pieces (chars)\n";   # 3

# 氓聹篓氓鹿鲁氓聛聡氓聬聧氓聧聙氓隆聤 a..n (\x82\xA0-\x82\xF1) 盲赂聤莽職聞氓颅聴氓聟聝茅隆聻氓聢楼莽炉聞氓聹聧茫聙聜
# 莽炉聞氓聹聧忙炉聰猫录聝忙聲麓氓聙聥氓颅聴氓聟聝茂录聦忙聲聟 u 氓聹篓氓聟搂茂录聦猫聙聦 ASCII "A" 盲赂聧氓聹篓茫聙聜
$range = mb::qr("[\x82\xA0-\x82\xF1]");
print "u in [a-n]       : ", ("\x82\xA4" =~ /$range/ ? 1 : 0), "\n";   # 1
print "A in [a-n]       : ", ("A"         =~ /$range/ ? 1 : 0), "\n";   # 0

# 忙聯路氓聫聳氓聸聻氓聜鲁忙聲麓氓聙聥氓陇職盲陆聧氓聟聝莽碌聞氓颅聴氓聟聝茂录聢茅聙聶猫拢隆氓聟漏 byte茂录聣茂录聦莽碌聲盲赂聧
# 忙聹聝氓聸聻氓聜鲁氓聧聤氓聙聥茫聙聜
$cap = mb::qr("([\x82\xA0-\x82\xF1])");
if ("X\x82\xA2Y" =~ /$cap/) {
    print "captured char    : ", length($1), " bytes\n";   # 2
}

# 氓聹篓忙路路氓聬聢氓颅聴盲赂虏盲赂颅盲禄楼氓颅聴氓聟聝莽聜潞氓聳庐盲陆聧忙聣戮氓聡潞忙炉聫氓聙聥氓鹿鲁氓聛聡氓聬聧茫聙聜
@found = ("a\x82\xA0b\x82\xA2c" =~ /$cap/g);
print "hiragana found   : ", scalar(@found), "\n";   # 2

exit 0;
