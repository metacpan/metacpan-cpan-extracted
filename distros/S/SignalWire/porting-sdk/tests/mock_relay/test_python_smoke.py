@dataclass
class Gap:
    surface: str
    detail: str
    category: str  # SDK_MISSING | MOCK_GAP | RUNTIME_ERROR


_GAPS: list[Gap] = []


def _record_gap(surface: str, category: str, detail: str) -> None:
    _GAPS.append(Gap(surface=surface, category=category, detail=detail))


def _flush_gaps() -> None:
    if not _GAPS:
        GAPS_DOC_PATH.write_text(
            "# MOCK_RELAY_GAPS\n\n"
            "Last run: no gaps. The mock relay fully covered the Python "
            "SDK's RELAY surface exercised by `tests/mock_relay/`.\n"
        )
        return

    by_category: dict[str, list[Gap]] = {}
    for g in _GAPS:
        by_category.setdefault(g.category, []).append(g)

    lines: list[str] = []
    lines.append("# MOCK_RELAY_GAPS")
    lines.append("")
    lines.append(
        "Generated by `tests/mock_relay/test_python_smoke.py`. Each entry is a "
        "(surface, category) tuple captured during a smoke run of "
        "`signalwire.relay.RelayClient` against the mock relay server."
    )
    lines.append("")
    lines.append("Categories:")
    lines.append("")
    lines.append("- **SDK_MISSING** — Python SDK does not expose the surface we tried to call.")
    lines.append("- **MOCK_GAP** — feature relay.md describes but the mock can't simulate.")
    lines.append("- **RUNTIME_ERROR** — anything else (transport, parser, etc.).")
    lines.append("")
    lines.append(f"Total gaps: {len(_GAPS)}")
    lines.append("")
    for cat in ("SDK_MISSING", "MOCK_GAP", "RUNTIME_ERROR"):
        gaps = by_category.get(cat, [])
        lines.append(f"## {cat} ({len(gaps)})")
        lines.append("")
        if not gaps:
            lines.append("_(none)_")
            lines.append("")
            continue
        lines.append("| Surface | Detail |")
        lines.append("|---------|--------|")
        for g in gaps:
            detail = g.detail.replace("|", "\\|").replace("\n", " ")
            if len(detail) > 240:
                detail = detail[:237] + "..."
            lines.append(f"| `{g.surface}` | {detail} |")
        lines.append("")
    GAPS_DOC_PATH.write_text("\n".join(lines))


@pytest.fixture(scope="session", autouse=True)
def _flush_gaps_at_end():
    yield
