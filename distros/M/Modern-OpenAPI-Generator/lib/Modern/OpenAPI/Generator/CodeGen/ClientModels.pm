Modern::OpenAPI::Generator::CodeGen::ClientModels - Generate Moo C<::Model::*> classes

=head1 DESCRIPTION

Builds one class per concrete C<components/schemas> object definition (skips
C<allOf> / C<oneOf> / C<anyOf> for now).

=head2 generate

Class method. Arguments: C<writer>, C<spec>, C<base>. Also emits C<Client::Result>
and C<Model::Base> when schemas exist.

=cut
