package Chart::Plotly::Trace::Attribute::Domain;
use Moose;

our $VERSION = '0.013';    # VERSION

sub TO_JSON {
    my $self = shift;
    my %hash = %$self;
    if ( $self->can('type') && ( !defined $hash{'type'} ) ) {
        $hash{type} = $self->type();
    }
    return \%hash;
}

