# –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —á—Ç–æ —Ç–µ–≥ ‚Äì –æ–¥–∏–Ω–æ—á–Ω—ã–π
sub is_single_tag(;$) {
	exists $SINGLE_TAG{$_[0] // $_}
}

# –ó–∞–±—Ä–∞—Å—ã–≤–∞–µ—Ç —Ç–µ–≥ –≤ —Å—Ç–µ–∫ –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∑–∞–∫—Ä—ã—Ç—ã–µ
sub in_tag(\@$$) {
	my ($S, $tag, $atag) = @_;

	# –í—ã–±—Ä–∞—Å—ã–≤–∞–µ–º –∏–∑ —Å—Ç–µ–∫–∞ –ø—Ä–µ–¥—ã–¥—É—â–∏–π —Ç–µ–≥
	my @ret;
	push @ret, pop @$S while @$S and
		($TOP_NEW_TAG{ $S->[$#$S][0] })->{$tag};

	push @$S, [$tag, $atag] unless exists $SINGLE_TAG{$tag};

	@ret
}

# –í—ã–±—Ä–∞—Å—ã–≤–∞–µ—Ç —Ç–µ–≥ –∏–∑ —Å—Ç–µ–∫–∞. –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –≤—ã–¥–∞–≤–ª–µ–Ω–Ω—ã–µ –∏–º –∏ –≤—ã–±—Ä–∞—Å—ã–≤–∞–µ—Ç –∏—Å–∫–ª—é—á–µ–Ω–∏–µ, –µ—Å–ª–∏ —Ç–∞–∫–æ–≥–æ –≤ —Å—Ç–µ–∫–µ –Ω–µ—Ç
sub out_tag(\@$) {
	my ($S, $tag) = @_;

	# –≠—Ç–æ –æ–¥–∏–Ω–æ—á–Ω—ã–π —Ç–µ–≥ - –æ–Ω –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –∑–∞–∫—Ä—ã–≤–∞—é—â–∏–º
	die "</$tag> is a single tag - it cannot be a closing tag" if exists $SINGLE_TAG{$tag};

	# –∑–∞–∫—Ä—ã–≤–∞–µ–º –ø—Ä–µ–¥—ã–¥—É—â–∏–π, –µ—Å–ª–∏ –Ω—É–∂–Ω–æ
	my @ret;
	push @ret, pop @$S while @$S and
		($TOP_CLOSE_TAG{$S->[$#$S][0]})->{$tag};

	die "</$tag>, but stack is empty!" unless @$S;

	# —Ç–µ–≥ —Ä–∞–≤–µ–Ω –∑–∞–∫—Ä—ã–≤–∞—é—â–µ–º—É
	die "<$S->[$#$S][0]> in stack ne </$tag>!" if $S->[$#$S][0] ne $tag;

	push @ret, pop @$S;

	@ret
}

=pod
–†–∞–∑–±–∏–≤–∞–µ—Ç —Ç–µ–∫—Å—Ç –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü—ã —Å —É—á—ë—Ç–æ–º html-—Ç–µ–≥–æ–≤

1. html-—Ç–µ–≥ –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å —Ç–∞–∫ –∂–µ —Ä–∞–∑–Ω–µ—Å—ë–Ω –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü—ã.
