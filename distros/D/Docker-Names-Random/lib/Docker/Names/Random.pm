## no critic (Documentation::RequirePodAtEnd)
## no critic (Documentation::RequirePodSections)

use strict;
use warnings;
use utf8;    # The __DATA__ is UTF-8
use English qw(-no_match_vars);

# ABSTRACT: Create random strings like Docker does for container names.

our $VERSION = '0.0.1'; # VERSION: generated by DZP::OurPkgVersion

use Exporter 'import';
our @EXPORT_OK   = qw( docker_name );
our %EXPORT_TAGS = ( 'all' => [qw( docker_name )] );

use YAML::PP;
use Hash::Util qw( lock_keys );


# Internal functions

sub _get_data {
    my $ypp = YAML::PP->new;
    local $INPUT_RECORD_SEPARATOR = undef;
    my $local_data = <DATA>;
    my ($data) = $ypp->load_string($local_data);
    return $data;
}

sub new {
    my $class = shift;

    my $data = _get_data();
    my $self = { data => $data };
    bless $self, $class;
    lock_keys( %{$self} );
    return $self;
}


sub docker_name {
    my $self = shift;
    my $data;
    if ( defined $self ) {
        $data = $self->{'data'};
    }
    else {

        # We are not running as an object method
        $data = _get_data();
    }

    my ( $adj, $surname );

  CREATE_NAME:
    my $r_for_adj = int( rand( scalar @{ $data->{'adjectives'} } ) );
    $adj = $data->{'adjectives'}->[$r_for_adj]->{'word'};
    my $r_for_name = int( rand( scalar @{ $data->{'names'} } ) );
    $surname = $data->{'names'}->[$r_for_name]->{'surname'};

    if ( $adj eq 'boring' && $surname eq 'wozniak' ) {

        # Steve Wozniak is not boring
        goto 'CREATE_NAME';
    }

    return $adj . q{_} . $surname;
}


1;

