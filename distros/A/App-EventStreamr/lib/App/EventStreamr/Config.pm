has 'room'          => ( is => 'rw', lazy => 1, default => sub { 'test_room' } );
has 'record_path'   => ( is => 'rw', lazy => 1, default => sub { '/tmp/$room/$date' } );
has 'run'           => ( is => 'rw', lazy => 1, default => sub { '0' } );
has 'control'       => ( is => 'rw' );
has 'stream'        => ( is => 'rw' );
has 'youtube'       => ( is => 'rw' );
has 'sync'          => ( is => 'rw' );
