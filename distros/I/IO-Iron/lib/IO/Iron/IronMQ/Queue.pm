    $message->reservation_id($response_message->{'reservation_id'});
	$log->debugf( 'Touched IronMQ Message (queue name=%s; message id=%s.',
		$queue_name, $message->id() );

	$log->tracef( 'Exiting touch_message(): %s', 'undef' );
