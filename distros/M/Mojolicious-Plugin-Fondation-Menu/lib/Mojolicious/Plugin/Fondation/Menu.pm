=head1 DESCRIPTION

L<Mojolicious::Plugin::Fondation::Menu> provides dynamic menu management.
Menus are declared declaratively via C<share/menus.json> in each plugin
and synchronized with the database via C<menu sync>.

=head2 Schema

  id          INTEGER PRIMARY KEY AUTOINCREMENT
  title       TEXT NOT NULL
  link        TEXT
  icon        TEXT          (FontAwesome class)
  icon_color  TEXT          (CSS color)
  name        TEXT NOT NULL DEFAULT 'left'  (menu group)
  condition   TEXT          ("group:NAME", "perm:NAME", or "")
  sort_order  INTEGER DEFAULT 0
  parent_id   INTEGER NOT NULL DEFAULT 0
  open_tab    INTEGER DEFAULT 0
  view_in_menu INTEGER DEFAULT 1
  description TEXT
  created_at  TEXT
  updated_at  TEXT

=head2 menus.json format

Each plugin can ship a C<share/menus.json>:

  [
    {
      "title":        "Administration",
      "link":         "/admin",
      "icon":         "fas fa-lock-open",
      "menu":         "admin_menu",
      "condition":    "group:administrateur",
      "order":        1,
      "parent_title": null,
      "description":  "Admin panel"
    },
    {
      "title":        "Users",
      "link":         "/users",
      "menu":         "admin_menu",
      "parent_title": "Administration"
    }
  ]

Fields: C<title> (required), C<link>, C<icon>, C<icon_color>,
C<menu> (group name, default "left"), C<condition>, C<order>
(auto if absent), C<parent_title>, C<open_tab>, C<view_in_menu>,
C<description>.

=head2 Commands

  myapp.pl menu sync       Sync menu entries from share/menus.json

