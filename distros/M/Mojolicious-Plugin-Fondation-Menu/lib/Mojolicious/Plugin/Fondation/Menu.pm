package Mojolicious::Plugin::Fondation::Menu;
$Mojolicious::Plugin::Fondation::Menu::VERSION = '0.02';
# ABSTRACT: Dynamic menu management plugin for Fondation — navbar, breadcrumb

use Mojo::Base 'Mojolicious::Plugin', -signatures;

sub fondation_meta {
    return {
        dependencies => [
            'Fondation::Model::DBIx::Async',
            'Fondation::MigrationDBIx',
            'Fondation::Layout::Bootstrap',
        ],
        defaults => {
            title  => 'Menu Management',
            models => {
                menu => { source => 'Menu', backend => undef },
            },
            fondation_init    => [
                ['menu', 'sync'],
            ],
            fondation_upgrade => [
                ['menu', 'sync'],
            ],
        },
    };
}

sub register ($self, $app, $conf) {

    # ── Register command namespace ───────────────────────────────────
    push @{$app->commands->namespaces},
        'Mojolicious::Plugin::Fondation::Menu::Command';

    # ── Helpers ──────────────────────────────────────────────────────
    require Mojolicious::Plugin::Fondation::Menu::Helpers;
    Mojolicious::Plugin::Fondation::Menu::Helpers->register_all($app);

    # ── Admin route (HTML) ───────────────────────────────────────────
    $app->routes->get('/menus')
        ->requires('fondation.perm' => 'menu_read')
        ->to(cb => sub ($c) {
            $c->render(template => 'menu/admin');
        });

    return $self;
}

1;

